#Opération logique and / or / not

result = (4 < 2) and (6 > 4) #il faut que ici les deux soient True pour avoir une réponse True avec le and
print(result)

result = result = (4 < 2) or (6 > 4)  #Si l'un des deux est True la réponse dans le code sera True avec le or
print(result)

result = not result #le not permet d'inverser le True et False
print (result)

result = not (10==4) #un autre exemple où le not inverse le False en True
